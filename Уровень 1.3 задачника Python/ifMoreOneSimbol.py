def print_second_to_last_char(text):
    """
    Проверяет длину строки. Если длина больше 1, выводит предпоследний символ;
    в противном случае выводит соответствующее сообщение.
    """
    # Проверяем условие: длина строки должна быть строго больше 1
    if len(text) > 1:
        # Используем индекс -2 для получения предпоследнего символа
        print("Предпоследний символ:", text[-2])
    elif len(text) == 1:
        print("Строка состоит всего из одного символа, нет предпоследнего.")
    else: # len(text) == 0 (пустая строка)
        print("Введенная строка пуста.")

# --- Тестирование функции ---

# 1. Случай, когда нужно вывести символ (длина > 1)
print("--- Тест 1 (Успех) ---")
input_string = "Python"
print(f"Исходная строка: '{input_string}'")
print_second_to_last_char(input_string)  # Ожидаемый вывод: o

print("-" * 20)

# 2. Случай, когда длина равна 1 (граничный случай)
print("--- Тест 2 (Длина = 1) ---")
input_string_single = "A"
print(f"Исходная строка: '{input_string_single}'")
print_second_to_last_char(input_string_single) # Ожидаемый вывод: сообщение об ошибке длины

print("-" * 20)

# 3. Случай, когда строка пустая (граничный случай)
print("--- Тест 3 (Длина = 0) ---")
input_string_empty = ""
print(f"Исходная строка: '{input_string_empty}'")
print_second_to_last_char(input_string_empty) # Ожидаемый вывод: сообщение о пустой строке
