def numGiven(num):
    """
    Возвращает последнюю цифру данного целого числа.
    """
    if not isinstance(num, int):
        return "Please enter an integer"
    
    # Оператор % 10 дает остаток от деления на 10, что и является последней цифрой.
    # Это работает как для положительных, так и для отрицательных чисел (в Python).
    last_digit = num % 10

    # Возвращаем результат в виде строки, чтобы соответствовать ожидаемому выводу "цифры"
    return str(last_digit)

# --- Тестирование функции ---

# Пример 1: Положительное число
number1 = 54321
result1 = numGiven(number1)
print(f"Число: {number1}, Последняя цифра: {result1}") # Ожидаем: 1

# Пример 2: Однозначное число
number2 = 7
result2 = numGiven(number2)
print(f"Число: {number2}, Последняя цифра: {result2}") # Ожидаем: 7

# Пример 3: Число, оканчивающееся на 0
number3 = 12340
result3 = numGiven(number3)
print(f"Число: {number3}, Последняя цифра: {result3}") # Ожидаем: 0

# Пример 4: Отрицательное число (Важно! Python обрабатывает это корректно)
# -123 % 10 = 7 при делении в математике, но в программировании нам нужна абсолютная цифра.
# В Python (-123) % 10 даст 7, потому что: -13 * 10 + 7 = -123.
# Если требуется строго положительная последняя цифра (как бы отбрасывая знак), нужно брать abs() перед операцией.

number4 = -98765
result4 = numGiven(number4)
print(f"Число: {number4}, Последняя цифра: {result4}") # Ожидаем: 5 (по модулю, или используя abs())


# Улучшенная версия для гарантии положительной последней цифры при отрицательном вводе:
def numGiven_improved(num):
    if not isinstance(num, int):
        return "Please enter an integer"
    
    # Работаем с абсолютным значением, чтобы знак не исказил результат % 10
    last_digit = abs(num) % 10
    return str(last_digit)

print("\n--- Проверка для отрицательных чисел (Улучшенная версия) ---")
number4_improved = -98765
result4_improved = numGiven_improved(number4_improved)
print(f"Число: {number4_improved}, Последняя цифра: {result4_improved}")
