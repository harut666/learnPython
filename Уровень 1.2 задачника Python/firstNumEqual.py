def firstNumEqual(numberOne, numberTwo):
    """
    Проверяет, совпадают ли первые цифры двух заданных целых чисел.
    """
    # Проверка типов данных
    if not isinstance(numberOne, int) or not isinstance(numberTwo, int):
        print("Both parameters should be integers.")
        return None
    
    # Получаем первые цифры, работая с абсолютным значением числа 
    # и преобразуя его в строку.
    FirstNum = str(abs(numberOne))[0]
    SecondNum = str(abs(numberTwo))[0]
    
    if FirstNum == SecondNum:
        print(f"Да, первые цифры чисел {numberOne} и {numberTwo} совпадают: '{FirstNum}'")
    else:      
        print(f"Нет, первые цифры чисел {numberOne} и {numberTwo} не совпадают. (Первая: '{FirstNum}', Вторая: '{SecondNum}')")

# Тестирование функции с примерами:

# Пример 1: Совпадают (23 и 145 -> обе начинаются на '2' и '1', но в примере выше предполагалось, что сравниваются числа, которые начинаются на одинаковые цифры)
# В данном случае '23' начинается на '2', а '145' начинается на '1'. Они не совпадают.
print("--- Тест 1 (Разные первые цифры) ---")
firstNumEqual(23, 145)  

# Пример 2: Совпадают (Оба начинаются на 5)
print("\n--- Тест 2 (Совпадающие первые цифры) ---")
firstNumEqual(500, 52)

# Пример 3: Не совпадают
print("\n--- Тест 3 (Несовпадающие первые цифры) ---")
firstNumEqual(1, 99)

# Пример 4: Обработка отрицательных чисел (работает корректно благодаря abs())
print("\n--- Тест 4 (С учетом знака) ---")
firstNumEqual(-350, 35)

# Пример 5: Неправильный ввод типа данных
print("\n--- Тест 5 (Ошибка типов) ---")
firstNumEqual("abc", 123)
