def first_last_num_sum(nums):
    """
    Вычисляет сумму первой и последней цифры заданного числа.
    Возвращает сумму или None, если ввод некорректен.
    """
    # 1. Проверка типа данных
    if not isinstance(nums, int):
        print("Ошибка: Ввод должен быть целым числом.")
        return None

    # Обработка случая с нулём (чтобы избежать ошибок при строке "0")
    if nums == 0:
        return 0 # Первая и последняя цифра - 0, сумма = 0

    # Преобразование числа в строку для доступа к первой и последней цифре
    s_nums = str(abs(nums))  # Используем abs() для корректной обработки отрицательных чисел (игнорируя знак)

    # Извлечение первых и последних символов, преобразование их обратно в int
    first_digit = int(s_nums[0])
    last_digit = int(s_nums[-1])

    # Вычисление и возврат суммы
    return first_digit + last_digit

# Примеры использования:
print(f"Сумма для 1234: {first_last_num_sum(1234)}")  # Ожидаемый вывод: 5
print(f"Сумма для 9: {first_last_num_sum(9)}")        # Ожидаемый вывод: 18 (9 + 9) - Поправка: должно быть 9+9=18, но если число состоит из одной цифры, первая и последняя это одна и та же. В данном случае, логично считать сумму первой и последней позиции, что в случае 9 будет 9+9=18 или просто 9? Согласно вашей логике (str[0] + str[-1]), для '9' будет 9+9=18. Если вы хотите, чтобы для одной цифры сумма была равна самой цифре, нужна дополнительная проверка. Оставим вашу изначальную логику.)
print(f"Сумма для 50: {first_last_num_sum(50)}")    # Ожидаемый вывод: 5 (5 + 0)
print(f"Сумма для -456: {first_last_num_sum(-456)}") # Ожидаемый вывод: 10 (Используется abs())
print(f"Сумма для нечисла ('abc'): {first_last_num_sum('abc')}") # Ожидаемый вывод: С ошибкой и None
