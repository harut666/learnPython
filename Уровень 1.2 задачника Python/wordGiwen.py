def word_given(text, text2):
    """
    Проверяет, совпадают ли первые буквы двух переданных строк.
    """
    # 1. Проверка типов: Убеждаемся, что оба аргумента являются строками (str)
    if not isinstance(text, str) or not isinstance(text2, str):
        print("❌ Ошибка ввода: Оба входных параметра должны быть строками.")
        return None

    # 2. Проверка на пустые строки (Обработка крайнего случая)
    if not text or not text2:
        print("⚠️ Предупреждение: Одна или обе строки пустые, сравнение невозможно.")
        return

    # 3. Извлечение первых букв
    word1_first = text[0]
    word2_first = text2[0]
    
    # 4. Сравнение и вывод результата (Логика верна!)
    if word1_first == word2_first:
        print(f"✅ '{text}' и '{text2}' начинаются на одну букву ({word1_first}).")
    else:    
        print(f"❌ '{text}' и '{text2}' начинаются на разные буквы соответственно ('{word1_first}' и '{word2_first}').")

# --- ТЕСТЫ ---

print("--- Тест 1: Совпадают ---")
word_given("apple", "antelope") # Ожидание: Совпадение 'a'

print("\n--- Тест 2: Различаются ---")
word_given("book", "car")       # Ожидание: Различия ('b' и 'c')

print("\n--- Тест 3: Одна пустая строка (Edge Case) ---")
word_given("hello", "")         # Ожидание: Предупреждение и возврат

print("\n--- Тест 4: Неверный тип данных (Error Handling) ---")
word_given("test", 123)        # Ожидание: Ошибка типов

